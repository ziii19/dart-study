void main() {

  // sama dengan
  if (4 == 3) {
    print('Ya, keduanya bernilai sama');
  }
  else{
    print('anda Salah!');
  }

  // tidak sama dengan
  if (4 != 3) {
    print('Ya, 2 tidak sama dengan 3');
  }
  else{
    print('anda Salah!');
  }

  // lebih dari
  if (4 > 3) {
    print('Ya, 2 lebih dari 3');
  }
  else{
    print('anda Salah!');
  }

  // kurang dari
  if (4 < 3) {
    print('Ya, 2 kurang dari 3');
  }
  else{
    print('anda Salah!');
  }

  // lebih dari sama dengan 
  if (4 >= 3) {
    print('Ya, 2 lebih dari sama dengan 3');
  }
  else{
    print('anda Salah!');
  }

  // kurang dari sama dengan 
  if (4 <= 3) {
    print('Ya, 2 lebih dari sama dengan 3');
  }
  else{
    print('anda Salah!');
  }
}